const a = 7 // = é uma atribuição
let b = 3

b += a // atribuição aditiva, o mesmo que b = b + a
console.log(b)

b -= 4 // atribução subtrativa, o mesmo que b = b - 4
console.log(b)

b *= 2 // atribuição multiplicativa, o mesmo que b = b * 2
console.log(b)

b /= 2 // atribuição divisiva, o mesmo que b = b / 2
console.log(b)

b %= 2 // atribuição modular, o mesmo que b = b % 2 (resto da divisão)
console.log(b)
